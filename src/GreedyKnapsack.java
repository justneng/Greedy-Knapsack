public class GreedyKnapsack {

    private int choice;
    private int weight;
    private int price;
    private int backWeight;
    private double pricePerWeight;

    public int getChoice() {
        return choice;
    }

    public void setChoice(int choice) {
        this.choice = choice;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getBackWeight() {
        return backWeight;
    }

    public void setBackWeight(int backWeight) {
        this.backWeight = backWeight;
    }

    public double getPricePerWeight() {
        return pricePerWeight;
    }

    public void setPricePerWeight(double pricePerWeight) {
        this.pricePerWeight = pricePerWeight;
    }
}
